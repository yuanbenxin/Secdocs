<ul>
<li>
<p><strong>动画效果</strong>：</p>
<ul>
<li><strong>滚动效果</strong>：名单快速滚动后停止在结果上</li>
<li><strong>闪烁效果</strong>：随机高亮闪烁人员，逐渐减速直至停止</li>
<li><strong>渐显效果</strong>：结果人员逐渐清晰显示</li>
<li><strong>无动画</strong>：直接显示结果，适合需要快速抽取的场景</li>
<li>可调节动画持续时间（1-5秒）</li>
</ul>
</li>
<li>
<p><strong>结果显示</strong>：</p>
<ul>
<li><strong>显示顺序</strong>：抽取顺序/姓名排序/编号排序</li>
<li><strong>显示信息</strong>：选择结果中显示的人员信息（姓名/编号/部门等）</li>
<li><strong>字体大小</strong>：调整结果显示文字大小</li>
<li><strong>背景样式</strong>：结果区域的背景效果（高亮/边框/无）</li>
</ul>
</li>
<li>
<p><strong>音效设置</strong>：</p>
<ul>
<li><strong>抽取开始提示音</strong>：开启/关闭</li>
<li><strong>抽取过程音效</strong>：开启/关闭</li>
<li><strong>结果揭晓音效</strong>：开启/关闭</li>
<li>可选择不同音效方案</li>
</ul>
</li>
<li>
<p><strong>防重复设置</strong>：</p>
<ul>
<li><strong>会话内防重复</strong>：同一抽取会话中不重复抽取同一人</li>
<li><strong>绝对防重复</strong>：无论何时都不重复抽取同一人（需手动重置）</li>
<li><strong>重复间隔</strong>：设置允许重复抽取的最短间隔次数</li>
</ul>
</li>
</ul>
<h3 id="_4-1-3-执行抽取操作" tabindex="-1"><a class="header-anchor" href="#_4-1-3-执行抽取操作"><span>4.1.3 执行抽取操作</span></a></h3>
<p>完成名单准备和参数设置后，即可开始抽取：</p>
<h4 id="单次抽取流程" tabindex="-1"><a class="header-anchor" href="#单次抽取流程"><span>单次抽取流程</span></a></h4>
<ol>
