<h3 id="_4-1-2-抽人参数设置" tabindex="-1"><a class="header-anchor" href="#_4-1-2-抽人参数设置"><span>4.1.2 抽人参数设置</span></a></h3>
<p>在开始抽取前，可根据需求自定义多种抽取参数，以满足不同场景的需求：</p>
<h4 id="基本设置" tabindex="-1"><a class="header-anchor" href="#基本设置"><span>基本设置</span></a></h4>
<p>在抽人模式界面右侧的「抽取设置面板」中进行配置：</p>
<ul>
<li>
<p><strong>抽取人数</strong>：</p>
<ul>
<li>通过数字输入框或上下箭头设置</li>
<li>范围：1-100人</li>
<li>可直接输入数字或使用滑块调整</li>
<li>下方显示当前设置占总人数的百分比</li>
</ul>
</li>
<li>
<p><strong>抽取模式</strong>：</p>
<ul>
<li><strong>单次抽取</strong>：一次性抽取指定数量的人员</li>
<li><strong>连续抽取</strong>：逐个抽取，每次抽取1人，共抽取指定次数</li>
<li><strong>随机排序</strong>：将所有人员随机排序，不指定具体抽取人数</li>
<li><strong>分组抽取</strong>：按预设分组分别抽取指定人数</li>
</ul>
</li>
<li>
<p><strong>抽取范围</strong>：</p>
<ul>
<li><strong>全部人员</strong>：从当前名单所有人员中抽取</li>
<li><strong>指定分组</strong>：仅从 selected分组中抽取</li>
<li><strong>排除已抽</strong>：排除本次会话中已抽取过的人员</li>
<li><strong>自定义范围</strong>：手动选择参与抽取的人员</li>
</ul>
</li>
</ul>
<h4 id="高级设置" tabindex="-1"><a class="header-anchor" href="#高级设置"><span>高级设置</span></a></h4>
