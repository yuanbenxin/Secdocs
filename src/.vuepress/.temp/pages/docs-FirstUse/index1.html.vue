<h4 id="_4-1-1-1-创建-导入名单" tabindex="-1"><a class="header-anchor" href="#_4-1-1-1-创建-导入名单"><span>4.1.1.1 创建/导入名单</span></a></h4>
<p>SecRandom支持多种方式管理抽人名单：</p>
<h5 id="创建新名单" tabindex="-1"><a class="header-anchor" href="#创建新名单"><span>创建新名单</span></a></h5>
