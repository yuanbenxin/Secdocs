<h4 id="名单导入失败" tabindex="-1"><a class="header-anchor" href="#名单导入失败"><span>名单导入失败</span></a></h4>
<ul>
<li><strong>原因</strong>：文件格式错误、字段不匹配、文件编码问题</li>
<li><strong>解决</strong>：
<ul>
<li>检查文件是否符合支持的格式要求</li>
<li>确保CSV/TXT文件使用正确的分隔符</li>
<li>尝试另存文件为UTF-8编码</li>
<li>简化文件名，避免特殊字符</li>
</ul>
</li>
</ul>
<h4 id="抽取结果重复" tabindex="-1"><a class="header-anchor" href="#抽取结果重复"><span>抽取结果重复</span></a></h4>
<ul>
<li><strong>原因</strong>：未启用防重复设置、防重复范围设置不当</li>
<li><strong>解决</strong>：
<ul>
<li>在高级设置中启用「会话内防重复」</li>
<li>如需完全避免重复，选择「绝对防重复」</li>
<li>点击「重置重复记录」可清除历史重复记录</li>
</ul>
</li>
</ul>
<h4 id="名单无法保存" tabindex="-1"><a class="header-anchor" href="#名单无法保存"><span>名单无法保存</span></a></h4>
<ul>
<li><strong>原因</strong>：名单名称重复、权限问题、存储空间不足</li>
<li><strong>解决</strong>：
<ul>
<li>更换不同的名单名称</li>
<li>检查软件数据目录权限</li>
<li>清理磁盘空间</li>
<li>尝试修复数据库（设置→维护→修复数据库）</li>
</ul>
</li>
</ul>
<h4 id="抽取动画卡顿" tabindex="-1"><a class="header-anchor" href="#抽取动画卡顿"><span>抽取动画卡顿</span></a></h4>
<ul>
<li><strong>原因</strong>：电脑配置较低、动画效果设置过复杂</li>
<li><strong>解决</strong>：
<ul>
<li>降低动画持续时间</li>
<li>选择更简单的动画效果</li>
<li>关闭音效减少系统资源占用</li>
<li>在设置中降低界面渲染质量</li>
</ul>
</li>
</ul>
<h2 id="_4-2-抽奖功能" tabindex="-1"><a class="header-anchor" href="#_4-2-抽奖功能"><span>4.2 抽奖功能</span></a></h2>
<p>抽奖功能适用于从设置好的奖品列表中随机抽取获奖者的场景，如会议抽奖、活动颁奖、节日抽奖等。相比抽人功能，抽奖功能增加了奖品管理、概率设置和多轮抽奖等特性。</p>
<h3 id="_4-2-1-奖品管理" tabindex="-1"><a class="header-anchor" href="#_4-2-1-奖品管理"><span>4.2.1 奖品管理</span></a></h3>
<p>使用抽奖功能前，需要先创建和管理奖品列表：</p>
<h4 id="创建奖品列表" tabindex="-1"><a class="header-anchor" href="#创建奖品列表"><span>创建奖品列表</span></a></h4>
<ol>
<li>在导航区点击「抽奖」按钮，进入抽奖模式界面</li>
<li>在左侧奖品管理区，点击「新建奖品列表」按钮</li>
<li>在弹出的对话框中：
<ul>
<li>输入奖品列表名称（如「年会抽奖」、「节日促销」等）</li>
<li>设置列表描述（可选）</li>
<li>选择奖品排序方式（默认/价值/概率）</li>
</ul>
</li>
<li>点击「确定」创建空白奖品列表</li>
</ol>
<h4 id="添加奖品" tabindex="-1"><a class="header-anchor" href="#添加奖品"><span>添加奖品</span></a></h4>
<ol>
<li>在空白奖品列表界面，点击「添加奖品」按钮（➕图标）</li>
<li>在添加奖品对话框中填写信息：
<ul>
<li><strong>奖品名称</strong>：必填项（如「iPhone 13」、「小米手环」等）</li>
<li><strong>奖品图片</strong>：可选项，点击上传图片</li>
<li><strong>数量</strong>：该奖品的总数量</li>
<li><strong>价值</strong>：奖品价值（用于排序和统计）</li>
<li><strong>中奖概率</strong>：可设置相对概率</li>
<li><strong>描述</strong>：奖品的详细说明（可选）</li>
<li><strong>备注</strong>：内部参考信息（可选）</li>
</ul>
</li>
<li>点击「确定」添加奖品</li>
<li>重复上述步骤添加更多奖品</li>
</ol>
<blockquote>
<p><strong>提示</strong>：添加多个奖品时，可使用「复制添加」功能（选中奖品后点击「复制」按钮）快速创建相似奖品。</p>
</blockquote>
<h4 id="奖品排序与分组" tabindex="-1"><a class="header-anchor" href="#奖品排序与分组"><span>奖品排序与分组</span></a></h4>
<ol>
<li><strong>手动排序</strong>：拖动奖品行可调整显示顺序</li>
<li><strong>自动排序</strong>：点击列表上方的排序按钮，可按以下方式排序：
<ul>
<li>按名称（A-Z或Z-A）</li>
<li>按价值（高到低或低到高）</li>
<li>按概率（高到低或低到高）</li>
<li>按数量（多到少或少到多）</li>
</ul>
</li>
<li><strong>奖品分组</strong>：
<ul>
<li>点击「创建奖品组」按钮</li>
<li>命名奖品组（如「一等奖」、「二等奖」等）</li>
<li>将奖品拖动到对应的组中</li>
<li>可设置每组的抽取顺序</li>
</ul>
</li>
</ol>
<h4 id="奖品编辑与管理" tabindex="-1"><a class="header-anchor" href="#奖品编辑与管理"><span>奖品编辑与管理</span></a></h4>
<ol>
<li><strong>编辑奖品</strong>：双击奖品行或选中后点击「编辑」按钮（✏️图标）</li>
<li><strong>删除奖品</strong>：选中奖品后点击「删除」按钮（🗑️图标）</li>
<li><strong>批量操作</strong>：按住Ctrl键选择多个奖品，进行批量编辑或删除</li>
<li><strong>导入导出</strong>：
<ul>
<li>点击「导入奖品」可从CSV/Excel文件导入奖品列表</li>
<li>点击「导出奖品」可将当前奖品列表保存为文件</li>
</ul>
</li>
<li><strong>复制奖品列表</strong>：点击「复制列表」按钮可快速创建当前列表的副本</li>
</ol>
<h3 id="_4-2-2-概率设置" tabindex="-1"><a class="header-anchor" href="#_4-2-2-概率设置"><span>4.2.2 概率设置</span></a></h3>
<p>抽奖功能的核心是概率设置，SecRandom提供多种灵活的概率配置方式：</p>
<h4 id="平均概率模式" tabindex="-1"><a class="header-anchor" href="#平均概率模式"><span>平均概率模式</span></a></h4>
<p>最简单的概率分配方式，所有奖品平分总中奖概率：</p>
<ol>
<li>在概率设置区选择「平均概率」</li>
<li>软件自动计算每个奖品的中奖概率：
<ul>
<li>公式：(1/奖品总数) × (奖品数量)</li>
<li>例如：5个奖品各1个，每个中奖概率为20%</li>
</ul>
</li>
<li>点击「应用」按钮确认设置</li>
</ol>
<h4 id="自定义概率模式" tabindex="-1"><a class="header-anchor" href="#自定义概率模式"><span>自定义概率模式</span></a></h4>
<p>允许手动设置每个奖品的相对中奖概率：</p>
<ol>
<li>在概率设置区选择「自定义概率」</li>
<li>每个奖品行将显示概率调整控件：
<ul>
<li><strong>滑块调节</strong>：拖动滑块设置相对概率（0-100）</li>
<li><strong>直接输入</strong>：点击概率值直接输入数字</li>
<li><strong>百分比模式</strong>：直接设置中奖百分比</li>
</ul>
</li>
<li>概率设置区顶部显示「概率总和」：
<ul>
<li>当选择「百分比模式」时，总和必须为100%</li>
<li>当选择「相对概率」时，总和可以是任意值，软件会自动计算比例</li>
</ul>
</li>
<li>可使用「自动分配」按钮快速分配剩余概率</li>
<li>点击「应用」保存设置</li>
</ol>
<blockquote>
<p><strong>提示</strong>：概率值越高，奖品被抽中的可能性越大。设置为0的奖品将不会被抽中。</p>
</blockquote>
<h4 id="权重概率模式" tabindex="-1"><a class="header-anchor" href="#权重概率模式"><span>权重概率模式</span></a></h4>
<p>基于奖品价值或其他因素自动计算概率：</p>
<ol>
<li>在概率设置区选择「权重概率」</li>
<li>选择权重依据：
<ul>
<li><strong>按价值权重</strong>：价值越高，概率越高</li>
<li><strong>按数量权重</strong>：数量越多，概率越高</li>
<li><strong>自定义权重</strong>：手动输入权重值</li>
</ul>
</li>
<li>设置权重曲线：
<ul>
<li>线性：概率与权重成正比</li>
<li>指数：放大高权重奖品的概率优势</li>
<li>对数：缩小高权重奖品的概率优势</li>
</ul>
</li>
<li>点击「计算概率」按钮，软件自动分配概率</li>
<li>可手动微调后点击「应用」</li>
</ol>
<h4 id="智能概率模式" tabindex="-1"><a class="header-anchor" href="#智能概率模式"><span>智能概率模式</span></a></h4>
<p>根据已抽奖结果动态调整后续概率，确保最终结果接近预期：</p>
<ol>
<li>在概率设置区选择「智能概率」</li>
<li>设置预期结果：
<ul>
<li>总体中奖率</li>
<li>各奖品的目标出现次数</li>
</ul>
</li>
<li>系统会在抽奖过程中动态调整概率：
<ul>
<li>已抽中次数少于预期的奖品，概率会提高</li>
<li>已抽中次数多于预期的奖品，概率会降低</li>
</ul>
</li>
<li>点击「应用」保存设置</li>
</ol>
<h3 id="_4-2-3-抽奖规则配置" tabindex="-1"><a class="header-anchor" href="#_4-2-3-抽奖规则配置"><span>4.2.3 抽奖规则配置</span></a></h3>
<p>抽奖功能提供丰富的规则设置，满足不同场景需求：</p>
<h4 id="基本抽奖规则" tabindex="-1"><a class="header-anchor" href="#基本抽奖规则"><span>基本抽奖规则</span></a></h4>
<p>在抽奖模式界面右侧的「抽奖设置」面板中配置：</p>
<ul>
<li>
<p><strong>抽奖轮次</strong>：</p>
<ul>
<li><strong>单轮抽奖</strong>：一次设置抽取所有奖品</li>
<li><strong>多轮抽奖</strong>：可设置多轮，每轮抽取不同奖品</li>
<li>可设置轮次名称（如「第一轮：幸运奖」、「第二轮：三等奖」等）</li>
</ul>
</li>
<li>
<p><strong>每轮抽取数量</strong>：</p>
<ul>
<li>设置每轮抽取的奖品数量</li>
<li>可设置固定数量或随机数量（如1-3个）</li>
<li>如奖品数量有限，会显示剩余可抽数量</li>
</ul>
</li>
<li>
<p><strong>抽奖模式</strong>：</p>
<ul>
<li><strong>普通模式</strong>：每次抽取一个奖品</li>
<li><strong>连环模式</strong>：一次抽取多个奖品，依次揭晓</li>
<li><strong>红包模式</strong>：同时抽取多个奖品，一起揭晓</li>
</ul>
</li>
<li>
<p><strong>参与限制</strong>：</p>
<ul>
<li><strong>无限制</strong>：同一参与者可多次中奖</li>
<li><strong>每人限中一次</strong>：同一参与者只能中一次奖</li>
<li><strong>每人限中n次</strong>：可自定义限制次数</li>
<li><strong>同名排除</strong>：排除姓名相同的参与者</li>
</ul>
</li>
</ul>
<h4 id="高级抽奖规则" tabindex="-1"><a class="header-anchor" href="#高级抽奖规则"><span>高级抽奖规则</span></a></h4>
<p>点击「高级规则」展开更多设置：</p>
<ul>
<li>
<p><strong>奖品释放规则</strong>：</p>
<ul>
<li><strong>顺序释放</strong>：按顺序抽取完一轮奖品后再开始下一轮</li>
<li><strong>随机释放</strong>：所有奖品混合在一起随机抽取</li>
<li><strong>分级释放</strong>：按奖品价值从低到高或从高到低抽取</li>
</ul>
</li>
<li>
<p><strong>结果显示设置</strong>：</p>
<ul>
<li><strong>显示中奖动画</strong>：开启/关闭</li>
<li><strong>显示概率变化</strong>：是否显示概率动态变化过程</li>
<li><strong>显示剩余数量</strong>：是否在抽奖过程中显示剩余奖品数量</li>
</ul>
</li>
<li>
<p><strong>抽奖控制设置</strong>：</p>
<ul>
<li><strong>手动停止</strong>：抽奖动画需手动点击停止</li>
<li><strong>自动停止</strong>：达到设定时间后自动停止</li>
<li><strong>速度控制</strong>：可调节抽奖动画速度</li>
</ul>
</li>
<li>
<p><strong>特殊规则</strong>：</p>
<ul>
<li><strong>必中规则</strong>：设置一定次数后必中特定奖品</li>
<li><strong>连中规则</strong>：设置是否允许连续抽中同一奖品</li>
<li><strong>间隔规则</strong>：设置同一奖品被抽中的最小间隔次数</li>
</ul>
</li>
</ul>
<h3 id="_4-2-4-执行抽奖操作" tabindex="-1"><a class="header-anchor" href="#_4-2-4-执行抽奖操作"><span>4.2.4 执行抽奖操作</span></a></h3>
<p>完成奖品设置和规则配置后，即可开始抽奖：</p>
<h4 id="单轮抽奖流程" tabindex="-1"><a class="header-anchor" href="#单轮抽奖流程"><span>单轮抽奖流程</span></a></h4>
<ol>
<li>确认已选择正确的奖品列表</li>
<li>设置当轮抽取奖品数量</li>
<li>点击中央的大型「开始抽奖」按钮
<ul>
<li>按钮为紫色圆形，位于界面中央</li>
<li>按钮文字为白色「开始抽奖」</li>
</ul>
</li>
<li>抽奖过程：
<ul>
<li>按钮变为「停止抽奖」</li>
<li>中央区域显示奖品快速滚动动画</li>
<li>可听到紧张的抽奖音效</li>
<li>状态栏显示抽奖进度</li>
</ul>
</li>
<li>停止抽奖：
<ul>
<li><strong>手动停止</strong>：点击「停止抽奖」按钮</li>
<li><strong>自动停止</strong>：达到设定时间后自动停止</li>
</ul>
</li>
<li>结果揭晓：
<ul>
<li>动画停止，显示中奖奖品</li>
<li>播放中奖音效</li>
<li>结果区域显示奖品信息</li>
<li>奖品列表中该奖品数量减少</li>
</ul>
</li>
</ol>
<h4 id="多轮抽奖流程" tabindex="-1"><a class="header-anchor" href="#多轮抽奖流程"><span>多轮抽奖流程</span></a></h4>
<ol>
<li>在抽奖规则中设置轮次数量和每轮奖品</li>
<li>点击「开始多轮抽奖」按钮</li>
<li>系统显示第一轮抽奖界面</li>
<li>完成第一轮抽奖后：
<ul>
<li>结果自动保存</li>
<li>界面显示「第一轮完成」</li>
<li>可选择「查看本轮详情」或「继续下一轮」</li>
</ul>
</li>
<li>重复上述步骤完成所有轮次</li>
<li>所有轮次完成后，显示「抽奖结束」和总结果统计</li>
</ol>
<h4 id="紧急暂停与恢复" tabindex="-1"><a class="header-anchor" href="#紧急暂停与恢复"><span>紧急暂停与恢复</span></a></h4>
<ol>
<li>抽奖过程中如需暂停，点击「暂停」按钮</li>
<li>动画暂停，界面显示「已暂停」</li>
<li>可选择：
<ul>
<li><strong>继续抽奖</strong>：从暂停处继续</li>
<li><strong>重新开始</strong>：放弃当前进度，重新开始本轮</li>
<li><strong>结束抽奖</strong>：结束当前轮次，保存已产生的结果</li>
</ul>
</li>
</ol>
<h3 id="_4-2-5-抽奖结果管理" tabindex="-1"><a class="header-anchor" href="#_4-2-5-抽奖结果管理"><span>4.2.5 抽奖结果管理</span></a></h3>
<p>抽奖完成后，可对结果进行多种操作和管理：</p>
<h4 id="查看结果" tabindex="-1"><a class="header-anchor" href="#查看结果"><span>查看结果</span></a></h4>
<ul>
<li>当前轮次结果显示在中央结果区域</li>
<li>已抽奖结果列表显示在右侧「中奖记录」区</li>
<li>每条记录包含：奖品名称、抽取时间、操作按钮</li>
<li>可点击「查看全部」查看完整结果列表</li>
</ul>
<h4 id="结果操作" tabindex="-1"><a class="header-anchor" href="#结果操作"><span>结果操作</span></a></h4>
<p>结果区域提供以下操作按钮：</p>
<ul>
<li>
<p><strong>添加获奖者信息</strong>：</p>
<ul>
<li>点击结果记录旁的「编辑」按钮</li>
<li>输入获奖者姓名、联系方式等信息</li>
<li>可上传获奖者照片（可选）</li>
<li>点击「保存」记录信息</li>
</ul>
</li>
<li>
<p><strong>导出结果</strong>：</p>
<ul>
<li>点击「导出结果」按钮（📤图标）</li>
<li>选择导出范围：当前轮次/所有轮次</li>
<li>选择导出格式：Excel/CSV/PDF/Word</li>
<li>设置保存路径和文件名</li>
<li>导出文件包含完整的奖品和获奖者信息</li>
</ul>
</li>
<li>
<p><strong>打印奖状</strong>：</p>
<ul>
<li>选中结果记录，点击「打印奖状」按钮</li>
<li>系统生成标准奖状格式</li>
<li>可预览并打印</li>
<li>支持自定义奖状模板</li>
</ul>
</li>
<li>
<p><strong>分享结果</strong>：</p>
<ul>
