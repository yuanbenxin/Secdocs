<li>设置名单描述（可选）</li>
</ul>
</li>
<li>点击「确定」按钮，创建空白名单</li>
</ol>
<h5 id="手动添加人员" tabindex="-1"><a class="header-anchor" href="#手动添加人员"><span>手动添加人员</span></a></h5>
