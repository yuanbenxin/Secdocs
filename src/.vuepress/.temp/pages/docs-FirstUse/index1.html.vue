<li>结果自动保存到历史记录</li>
</ul>
</li>
</ol>
<h4 id="连续抽取流程" tabindex="-1"><a class="header-anchor" href="#连续抽取流程"><span>连续抽取流程</span></a></h4>
<ol>
<li>在抽取模式中选择「连续抽取」</li>
<li>设置总抽取人数</li>
<li>点击「开始抽取」按钮</li>
<li>首次抽取结果显示后：
<ul>
<li>「开始抽取」按钮变为「抽取下一位」</li>
<li>已抽取人员会标记为灰色并移至结果区</li>
</ul>
</li>
<li>点击「抽取下一位」继续抽取</li>
<li>全部抽取完成后显示最终结果汇总</li>
</ol>
<h4 id="紧急停止抽取" tabindex="-1"><a class="header-anchor" href="#紧急停止抽取"><span>紧急停止抽取</span></a></h4>
